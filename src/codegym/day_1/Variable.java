package codegym.day_1;

public class Variable {
    public static void main(String[] args) {
        //khai báo biến: <kiểu dữ liệu> <name> = <giá trị>;
        // kiểu dữ liệu
        // byte: chứa số có dạng 8-bit
        // short: chứa số có dạng 16-bit
        // int: số nguyên; có dạng 32-bit; -2^31 -> 2^31-1
        // long: số nguyên: có dạng 64-bit: -2^63 -> 2^63-1
        // float: giống kích thước int; dạng số thập phân
        // double: giống kích thước long; dạng số thập phân
        // boolean: T/F
        // char: ký tự; đánh dấu bằng nháy đơn ''
        // string dùng nháy kép ""
        int number = 100;
        System.out.println(number);
        // JavaScript: toán học, so sánh, logic, gán, cộng chuỗi, type of
        // Java: toán học, so sánh, logic, gán, cộng chuỗi, instance of
        // toán tử so sánh
        // > < >= <= == !=
    }
}
